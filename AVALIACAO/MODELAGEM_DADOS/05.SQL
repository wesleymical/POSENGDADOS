-- Criação da tabela ELEIÇÃO
CREATE TABLE ELEICAO (
    CD_ELEICAO INT PRIMARY KEY,  
    ANO_ELEICAO INT,  
    DS_ELEICAO VARCHAR(255),  
    DT_ELEICAO DATE,  
    NM_TIPO_ELEICAO VARCHAR(255),  
    NR_TURNO INT,  
    TP_ABRANGENCIA VARCHAR(50),  
    CD_TIPO_ELEICAO INT
);

-- Criação da tabela CANDIDATO
CREATE TABLE CANDIDATO (
    SQ_CANDIDATO INT PRIMARY KEY,  
    NM_CANDIDATO VARCHAR(255),  
    CD_ELEICAO INT,  
    CD_COR_RACA INT,  
    DT_NASCIMENTO DATE,
    FOREIGN KEY (CD_ELEICAO) REFERENCES ELEICAO(CD_ELEICAO)
);

-- Criação da tabela VOTAÇÃO_ZONA
CREATE TABLE VOTACAO_ZONA (
    CD_MUNICIPIO INT,
    NM_MUNICIPIO VARCHAR(255),
    NR_ZONA INT,
    SQ_CANDIDATO INT,
    PRIMARY KEY (CD_MUNICIPIO, NR_ZONA, SQ_CANDIDATO),
    FOREIGN KEY (SQ_CANDIDATO) REFERENCES CANDIDATO(SQ_CANDIDATO)
);

-- Criação da tabela PARTIDO
CREATE TABLE PARTIDO (
    NR_PARTIDO INT PRIMARY KEY,  
    SG_PARTIDO VARCHAR(20),  
    NM_PARTIDO VARCHAR(255)
);

-- Criação da tabela COLIGAÇÃO
CREATE TABLE COLIGACAO (
    SQ_COLIGACAO INT PRIMARY KEY,  
    NM_COLIGACAO VARCHAR(255),  
    CD_TIPO_ELEICAO INT
);

-- Criação da tabela FEDERAÇÃO
CREATE TABLE FEDERACAO (
    NR_FEDERACAO INT PRIMARY KEY,  
    NM_FEDERACAO VARCHAR(255)
);

-- Criação da tabela BEM_CANDIDATO
CREATE TABLE BEM_CANDIDATO (
    SQ_CANDIDATO INT,
    NR_ORDEM_BEM_CANDIDATO INT,
    DS_BEM VARCHAR(255),
    VL_BEM DECIMAL(10, 2),
    PRIMARY KEY (SQ_CANDIDATO, NR_ORDEM_BEM_CANDIDATO),
    FOREIGN KEY (SQ_CANDIDATO) REFERENCES CANDIDATO(SQ_CANDIDATO)
);

-- Criação da tabela MOTIVO_CASSACAO
CREATE TABLE MOTIVO_CASSACAO (
    SQ_CANDIDATO INT,
    CD_MOTIVO INT,
    DS_MOTIVO VARCHAR(255),
    PRIMARY KEY (SQ_CANDIDATO, CD_MOTIVO),
    FOREIGN KEY (SQ_CANDIDATO) REFERENCES CANDIDATO(SQ_CANDIDATO)
);

-- Criação da tabela FEDERACAO_PARTIDO (tabela intermediária para o relacionamento)
CREATE TABLE FEDERACAO_PARTIDO (
    NR_FEDERACAO INT,
    NR_PARTIDO INT,
    PRIMARY KEY (NR_FEDERACAO, NR_PARTIDO),
    FOREIGN KEY (NR_FEDERACAO) REFERENCES FEDERACAO(NR_FEDERACAO),
    FOREIGN KEY (NR_PARTIDO) REFERENCES PARTIDO(NR_PARTIDO)
);